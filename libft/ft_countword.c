#include "libft.h"

int		ft_countword(const char *str, int c)
{
	int num;
	int i;

	i = 0;
	num = 0;
	while (str[i] != '\0')
	{
		if (str[i] != c)
		{
			while (str[i] != c)
				i++;
			num++;
		}
		i++;
	}
	return (num);
}
